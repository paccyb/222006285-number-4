// program uses switch case to display grades
public class switchexample { // PUBLIC CLASS SWITCH
 public static void main(String[]args){// MAIN METHOD
int marks=65;
switch (marks) {
    case 65: System.out.println("GET GRADE A");
    break;
    case 60: System.out.println("GET GRADE B");
    break;
    case 55: System.out.println("GET GRADE C");
    break;
    case 50: System.out.println("GET GRADE D");
    break;
    default: System.out.println("UN CLASSIFIED");
    
        
}
 }   
}
