public class switchcase {//public class switch case 
public static void main(String[]args){// main method
    int experience=5;// variable initialization
    switch (experience) {//  switch case intro 
        case 6: 
        System.out.println("REWARDS is 250000 RWF");// case6 output statement
        break;// break statement
        case 5:
        System.out.println("REWARDS is 200000 RWF");// case5 out put
        break;
        case 4 : 
        System.out.println("REWARDS is 150000 RWF");// case4 out put
        break;
        case 3 : 
        System.out.println("REWARDS is 100000 RWF");// case3 out put
        break;
        default:System.out.println("NO REWARDS");// default statement out put
             
    }
}    
}
